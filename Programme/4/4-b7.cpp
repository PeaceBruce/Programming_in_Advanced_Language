void convert(int n)
{
	int a;
	a = n % 10;
	if (n / 10 != 0)
		convert(n / 10);
	else {
		if (n < 0)
			cout << "-" << " ";
	}
	if (a < 0)
		a = -a;
	cout << char(a + '0') << " ";
}
 
